public class AzaryaPrimitives2 {
    public static void main(String[] args) {
        //declare variable
       int x = 2;
       int y = 10;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("");
        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (double)x / y);
    }

}
